package by.htp.les02.main;
//1. Даны два действительных числа х и у. Вычислить их сумму, разность, произведение и частное.

public class Task01 {

	public static void main(String[] args) {
		double x = 4.4;
		double y = 5.5;
		double resSum = x + y;
		double resDiff = x - y;
		double resMultiple = x * y;
		double resDivide = x * y;

		System.out.println("Sum is " + resSum + " , Diff is " + resDiff + " , Multiple is " + resMultiple
				+ " , Division is " + resDivide);

	}

}
